    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String cpf = jtfNome.getText();
        String nome = "";
        String caminho = ".\\src\\model\\clientes\\clientesCadastrados";
        String[] inf = {"",""};
        String conteudo;
        Arquivo arq = new Arquivo();
        int tam = arq.contarArquivos(caminho);
        int i = 0;
        String[] nomesArquivos = arq.mostrarAqrquivos(caminho);
        while(nome != inf[0] || i<tam){
            conteudo = arq.Read(caminho + "\\" + nomesArquivos[i]);
            inf = conteudo.split("\n");
            if(cpf.equals(inf[1])){
                nome = inf[0]; 
                this.setVisible(false);
                new JanelaListagemVendasCliente2().setVisible(true);
                new JanelaListagemVendasCliente2().dispose();
                arq.Write(".\\src\\model\\cpf", cpf);
            }
            i++;
            if(i == tam){
                JOptionPane.showMessageDialog(null, "Cliente nÃ£o encontrado");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

